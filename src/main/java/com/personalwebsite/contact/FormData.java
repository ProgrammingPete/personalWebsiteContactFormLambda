package com.personalwebsite.contact;

public class FormData {
    private String firstName;
    private String lastName;
    private String email;
    private String phone;
    private String company;
    private String title;
    private String industry;
    private String productLines;
    private String quotingProcess;
    private String message;

    public FormData() {
    }

    public String getFirstName() {
        return firstName;
    }

    public void setFirstName(String firstName) {
        this.firstName = firstName;
    }

    public String getLastName() {
        return lastName;
    }

    public void setLastName(String lastName) {
        this.lastName = lastName;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getCompany() {
        return company;
    }

    public void setCompany(String company) {
        this.company = company;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getIndustry() {
        return industry;
    }

    public void setIndustry(String industry) {
        this.industry = industry;
    }

    public String getProductLines() {
        return productLines;
    }

    public void setProductLines(String productLines) {
        this.productLines = productLines;
    }

    public String getQuotingProcess() {
        return quotingProcess;
    }

    public void setQuotingProcess(String quotingProcess) {
        this.quotingProcess = quotingProcess;
    }

    public String getMessage() {
        return message;
    }

    public void setMessage(String message) {
        this.message = message;
    }
}
